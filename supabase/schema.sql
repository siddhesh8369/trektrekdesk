create or replace function public.publish_submission(target_submission_id bigint)
returns bigint
language plpgsql
security definer
set search_path = public, private
as $$
declare
  candidate public.trek_submissions%rowtype;
  published_trek_id bigint;
  slug_base text;
begin
  if not private.is_admin() then
    raise exception 'Admin access required';
  end if;

  select *
  into candidate
  from public.trek_submissions
  where id = target_submission_id;

  if not found then
    raise exception 'Submission not found';
  end if;

  slug_base := trim(both '-' from regexp_replace(lower(candidate.name), '[^a-z0-9]+', '-', 'g'));

  if slug_base = '' then
    slug_base := 'trek';
  end if;

  insert into public.treks (
    source_submission_id,
    slug,
    name,
    region,
    country,
    difficulty,
    duration_days,
    max_altitude_m,
    distance_km,
    best_months,
    price_from_inr,
    risk_level,
    summary,
    image_url,
    image_alt,
    highlights,
    essentials,
    waypoints,
    published
  )
  values (
    candidate.id,
    slug_base || '-' || candidate.id::text,
    candidate.name,
    candidate.region,
    candidate.country,
    candidate.difficulty,
    candidate.duration_days,
    candidate.max_altitude_m,
    candidate.distance_km,
    candidate.best_months,
    candidate.price_from_inr,
    candidate.risk_level,
    candidate.summary,
    candidate.image_url,
    candidate.image_alt,
    candidate.highlights,
    candidate.essentials,
    candidate.waypoints,
    true
  )
  on conflict (source_submission_id) do update set
    name = excluded.name,
    region = excluded.region,
    country = excluded.country,
    difficulty = excluded.difficulty,
    duration_days = excluded.duration_days,
    max_altitude_m = excluded.max_altitude_m,
    distance_km = excluded.distance_km,
    best_months = excluded.best_months,
    price_from_inr = excluded.price_from_inr,
    risk_level = excluded.risk_level,
    summary = excluded.summary,
    image_url = excluded.image_url,
    image_alt = excluded.image_alt,
    highlights = excluded.highlights,
    essentials = excluded.essentials,
    waypoints = excluded.waypoints,
    published = true
  returning id into published_trek_id;

  update public.trek_submissions
  set
    status = 'approved',
    reviewed_by = (select auth.uid()),
    reviewed_at = now()
  where id = candidate.id;

  return published_trek_id;
end;
$$;

revoke all on function public.publish_submission(bigint) from public;
grant execute on function public.publish_submission(bigint) to authenticated;
