create schema if not exists private;

revoke all on schema private from public;
grant usage on schema private to authenticated;

create table if not exists public.profiles (
  id uuid primary key references auth.users (id) on delete cascade,
  display_name text,
  role text not null default 'trekker' check (role in ('trekker', 'admin')),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

alter table public.profiles enable row level security;

create or replace function private.is_admin()
returns boolean
language sql
security definer
set search_path = public
stable
as $$
  select exists (
    select 1
    from public.profiles
    where id = (select auth.uid())
      and role = 'admin'
  );
$$;

revoke all on function private.is_admin() from public;
grant execute on function private.is_admin() to authenticated;

revoke all on table public.profiles from anon, authenticated;
grant select on table public.profiles to authenticated;

drop policy if exists "Profiles are visible to their owners and admins" on public.profiles;
create policy "Profiles are visible to their owners and admins"
on public.profiles
for select
to authenticated
using ((select auth.uid()) = id or private.is_admin());

create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.profiles (id, display_name)
  values (new.id, nullif(new.raw_user_meta_data ->> 'full_name', ''))
  on conflict (id) do nothing;
  return new;
end;
$$;

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
after insert on auth.users
for each row
execute function public.handle_new_user();

create table if not exists public.trek_submissions (
  id bigint generated by default as identity primary key,
  submitted_by uuid not null default auth.uid() references auth.users (id) on delete cascade,
  name text not null,
  region text not null,
  country text not null,
  difficulty text not null check (difficulty in ('Easy', 'Moderate', 'Difficult')),
  duration_days integer not null check (duration_days > 0),
  max_altitude_m integer not null check (max_altitude_m > 0),
  distance_km integer not null check (distance_km > 0),
  best_months text[] not null default '{}',
  price_from_inr integer not null check (price_from_inr >= 0),
  risk_level text not null,
  summary text not null,
  image_url text not null,
  image_alt text not null,
  highlights text[] not null default '{}',
  essentials text[] not null default '{}',
  waypoints text[] not null default '{}',
  status text not null default 'pending' check (status in ('pending', 'approved', 'rejected')),
  admin_notes text,
  reviewed_by uuid references auth.users (id),
  reviewed_at timestamptz,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.treks (
  id bigint generated by default as identity primary key,
  source_submission_id bigint unique references public.trek_submissions (id),
  slug text not null unique,
  name text not null,
  region text not null,
  country text not null,
  difficulty text not null check (difficulty in ('Easy', 'Moderate', 'Difficult')),
  duration_days integer not null check (duration_days > 0),
  max_altitude_m integer not null check (max_altitude_m > 0),
  distance_km integer not null check (distance_km > 0),
  best_months text[] not null default '{}',
  price_from_inr integer not null check (price_from_inr >= 0),
  risk_level text not null,
  summary text not null,
  image_url text not null,
  image_alt text not null,
  highlights text[] not null default '{}',
  essentials text[] not null default '{}',
  waypoints text[] not null default '{}',
  signal integer not null default 80 check (signal between 0 and 100),
  featured_rank integer not null default 100,
  published boolean not null default false,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

